void main()
{	
	
	unsigned char i,offset=0,count=0;
	MatrixLED_Init();	
	while(1)
	{
		for(i=0;i<64;i++)
		{
			MatrixLED_ShowColumn(AniMation[i+offset],i);
		}
		count++;
		if(count>1)
		{
			count=0;
			offset++;
			if(offset>64)
				offset=0;
			
		}
       
	}
}

